// Escreva uma função que receba um número negativo e retorne um número positivo;
// DICA: Math.abs

function numPositivo(numNegativo){
    return Math.abs(numNegativo);
}

console.log(numPositivo(-10));
console.log(numPositivo(-4));
console.log(numPositivo(-4.5));
console.log(numPositivo(-55.4));
console.log(numPositivo(100));